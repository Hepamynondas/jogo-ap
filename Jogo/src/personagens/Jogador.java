package personagens;

public class Jogador {
	protected String nome;
	protected int sexo;
	protected int raça;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getSexo() {
		return sexo;
	}
	public void setSexo(int sexo) {
		this.sexo = sexo;
	}
	public int getRaça() {
		return raça;
	}
	public void setRaça(int raça) {
		this.raça = raça;
	}
	
	
}
